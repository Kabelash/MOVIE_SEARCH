package com.kabelash.moviessearch.model;

public class Search {
    String Title;
    String Year;
    String imdbID;
    String Type;
    String Poster;

    public Search() {
    }

    public String getTitle() {
        return Title;
    }

    public String getYear() {
        return Year;
    }

    public String getimdbID() {
        return imdbID;
    }

    public String getType() {
        return Type;
    }

    public String getPoster() {
        return Poster;
    }

}
